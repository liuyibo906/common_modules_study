import sys
#解释器版本
print(sys.version)
#python最大的数值
print(sys.maxsize)
#python 查找模块的路径，返回列表
print(sys.path)
#windows版本
print(sys.platform)
#argv 参数
print(sys.argv)
#默认编码
print(sys.getdefaultencoding())
#默认文件编码
print(sys.getfilesystemencoding())
#递归调用次数限制
print(sys.getrecursionlimit())
#设置递归次数限制  sys.setrecursionlimit(200)

#exit
sys.exit(1)